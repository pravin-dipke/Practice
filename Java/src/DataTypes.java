public class DataTypes{
    public static void main(String[] args){
        //Primitive Data Types
        //Boolean
        boolean value = true;
        //character
        char letter = 'A';
        //integer
        int num = 3;
        //long
        long num1 = 18193847;
        //byte
        byte num2 = 4;
        //short
        short num3 = 56;
        //float
        float fnum = 3.134f;
        //double
        double dnum = 3.09738130487;
        String greetings = "Hello, World! ";
        System.out.println("String:- " + greetings);
        System.out.println("Boolean:- " + value);
        System.out.println("char:- " + letter);
        System.out.println("integer:- " + num);
        System.out.println("long:- " + num1);
        System.out.println("byte:- " + num2);
        System.out.println("short:- " + num3);
        System.out.println("float:- " + fnum);
        System.out.println("doble:- " + dnum);

    }
}
        
        
